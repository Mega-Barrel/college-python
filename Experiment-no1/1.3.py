# Write a program to implement List, Tuple, Dictionary and Array.

# 1. Lists:
thislist = ['apple', 'banana', 'cherry']
print(thislist)

print('------------/------------')

# 2. Tuple:
thistuple = ('Hello', 'world!')
print(thistuple)

print('------------/------------')

# 3. Dictionary:
thisdictionary = {'Have a good day!'}
print(thisdictionary)

print('------------/------------')

# 4. Array:
# Python does not have built-in support with array, but python list can be used.
thisarray = ["Ford", "Volvo", "BMW"]
for i in thisarray:
    print(i)
print('Length is',len(i))